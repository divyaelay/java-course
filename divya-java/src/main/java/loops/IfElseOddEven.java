package loops;

public class IfElseOddEven {

    public void oddOrEven(int number) {

        System.out.println("### Learning if Else ###");


        int result = number % 2;

        if (result == 0) {
            System.out.println("Entered number is " + number + ", its an even number");
        } else {
            System.out.println("Entered number is " + number + ", its an even number");
        }
    }
}
